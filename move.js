//Hide article
var art = document.getElementsByTagName("article");
var curArticle = new Array();
var curArticleNumber=0;


for(var i = 0; i< art.length; i++){
    if (art[i].className == "home_article") {
        if (i == 0) {
            curArticle[curArticleNumber++] = i;

        }

        else {
            curArticle[curArticleNumber++] = i;
            art[i].style.display = "none";
        }
    }
}


curArticleNumber = 0;


